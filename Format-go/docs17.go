package main

import "fmt"

func main() {
	// شروع یک حلقه از 0 تا 100
	for i := 0; i <= 100; i++ {

		// بررسی اینکه آیا عدد زوج است یا نه
		if i%2 == 0 {

			// اگر عدد 5 باشد، این تکرار از حلقه را رد می‌کند و به تکرار بعدی می‌رود
			if i == 5 {
				// دستور continue باعث می‌شود که کد بعد از آن در این تکرار اجرا نشود
				// و به تکرار بعدی حلقه برود
				continue
			}

			// اگر عدد 9 باشد، حلقه را به طور کامل متوقف می‌کند
			if i == 9 {
				// دستور break باعث می‌شود که حلقه کاملاً متوقف شود
				break
			}

			// در غیر اینصورت، عدد را چاپ می‌کند
			// این خط فقط برای اعدادی اجرا می‌شود که زوج باشند
			// و نه 5 و نه 9 باشند
			fmt.Println(i)
		}
	}
}
