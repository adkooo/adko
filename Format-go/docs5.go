package main

import "fmt"  // بسته fmt برای چاپ اطلاعات

func main() {

	a := "ali"  // تعریف متغیر a از نوع string و مقدار آن "ali"

	// چاپ طول رشته، نوع داده و اولین کاراکتر رشته
	// %d: برای نمایش طول رشته
	// %T: برای نمایش نوع داده متغیر
	// %c: برای نمایش اولین کاراکتر رشته به صورت کاراکتر
	// توجه: باید از fmt.Printf به جای fmt.Println برای قالب‌بندی استفاده شود
	fmt.Printf("%d %T %c\n", len(a), a, a[0])  // len(a): طول رشته، a: نوع داده، a[0]: اولین کاراکتر "a"
}
