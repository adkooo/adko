package main

import "fmt"

func main() {

	// یک حلقه for که از 0 شروع می‌کند و تا 10 ادامه دارد
	// در هر تکرار مقدار متغیر 'i' چاپ می‌شود
	for i := 0; i < 10; i++ {
		fmt.Println(i)
	}

	// تعریف یک آرایه یا لیست از اعداد صحیح
	list := []int{1, 2, 43, 4}

	// یک حلقه for برای پیمایش عناصر لیست با استفاده از range
	// متغیر 'b' نمایانگر ایندکس و 'c' نمایانگر مقدار عنصر در هر تکرار است
	for b, c := range list {
		fmt.Println(b, c) // چاپ ایندکس و مقدار مربوطه از لیست
	}

}
