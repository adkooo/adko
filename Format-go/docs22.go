package main

func main() {

	// شروع یک حلقه از 0 تا 9
	for i := 0; i < 10; i++ {
		// دستور defer باعث می‌شود که دستور بعد از آن تا پایان اجرای تابع به تعویق بیفتد
		// در اینجا، چاپ مقدار i به تعویق می‌افتد تا پس از اتمام حلقه انجام شود
		defer println(i)
	}

	// در اینجا از defer استفاده شده است، بنابراین مقادیر i از 9 به 0 چاپ می‌شوند
	// زیرا دستور defer به تعویق می‌افتد و ابتدا دستورهای defer اجرا می‌شوند.
}
