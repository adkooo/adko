package main // تعریف بسته اصلی برنامه

import "fmt" // وارد کردن بسته fmt برای انجام عملیات ورودی/خروجی (مثل چاپ در خروجی)

func main() { // نقطه ورود برنامه (تابع اصلی)

	// تعریف چندین ثابت به صورت گروهی
	const (
		name = "ali" // ثابت رشته‌ای که مقدار آن "ali" است
		m    = 7     // ثابت عدد صحیح با مقدار 7
	)

	// تعریف یک ثابت دیگر به صورت جداگانه با نوع مشخص
	const pi float64 = 3.14 // ثابت عدد اعشاری با نوع float64 و مقدار 3.14

	// چاپ مقادیر ثابت‌های تعریف‌شده
	// در این خط، pi و name به ترتیب در خروجی چاپ می‌شوند
	fmt.Println(pi, name)

	// استفاده از فرمت‌دهی برای چاپ مقادیر به صورت قالب‌بندی‌شده
	// %d: جایگاه برای عدد صحیح
	// %s: جایگاه برای رشته
	// %f: جایگاه برای عدد اعشاری
	// این خط دارای یک خطا است، زیرا fmt.Println نمی‌تواند مستقیماً قالب‌بندی با placeholderها را مدیریت کند
	fmt.Println("1 : %d 2 : %s \n 3 : %f", m, name, pi)
	// برای رفع خطا باید از fmt.Printf استفاده شود:
	// fmt.Printf("1 : %d 2 : %s \n 3 : %f", m, name, pi)

}
