package main

import "fmt"

func main() {

	// یک حلقه for که از 0 شروع شده و تا 100 ادامه دارد
	for i := 0; i <= 100; i++ {

		// بررسی اینکه آیا عدد زوج است (i % 2 == 0)
		if i%2 == 0 {
			// اگر عدد برابر با 5 باشد، از ادامه حلقه عبور می‌کند
			if i == 5 {
				continue
			}

			// اگر عدد برابر با 9 باشد، حلقه متوقف می‌شود
			if i == 9 {
				break
			}

			// چاپ اعداد زوج که شرایط بالا را نقض نمی‌کنند
			fmt.Println(i)
		}
	}

}
